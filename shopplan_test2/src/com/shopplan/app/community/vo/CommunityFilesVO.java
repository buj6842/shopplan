package com.shopplan.app.community.vo;

public class CommunityFilesVO {
	private String community_file_name;
	private int community_num;

	public CommunityFilesVO() {;}
	
	public String getCommunity_file_name() {
		return community_file_name;
	}
	public void setCommunity_file_name(String community_file_name) {
		this.community_file_name = community_file_name;
	}
	public int getCommunity_num() {
		return community_num;
	}
	public void setCommunity_num(int community_num) {
		this.community_num = community_num;
	}
	
}
