package com.shopplan.app.askhelp.vo;

public class AskhelpVO {
	private int askhelp_num;
	private String askhelp_title;
	private String askhelp_content;
	private String askhelp_id;
	private String askhelp_date;
	private int askhelp_readCount;
	
	public AskhelpVO() {;} 
	

	public int getAskhelp_num() {
		return askhelp_num;
	}

	public void setAskhelp_num(int askhelp_num) {
		this.askhelp_num = askhelp_num;
	}

	public String getAskhelp_title() {
		return askhelp_title;
	}

	public void setAskhelp_title(String askhelp_title) {
		this.askhelp_title = askhelp_title;
	}

	public String getAskhelp_content() {
		return askhelp_content;
	}

	public void setAskhelp_content(String askhelp_content) {
		this.askhelp_content = askhelp_content;
	}

	public String getAskhelp_id() {
		return askhelp_id;
	}

	public void setAskhelp_id(String askhelp_id) {
		this.askhelp_id = askhelp_id;
	}

	public String getAskhelp_date() {
		return askhelp_date;
	}

	public void setAskhelp_date(String askhelp_date) {
		this.askhelp_date = askhelp_date;
	}
	
	public int getReadCount() {
		return askhelp_readCount;
	}

	public void setReadCount(int askhelp_readCount) {
		this.askhelp_readCount = askhelp_readCount;
	}

	

}
